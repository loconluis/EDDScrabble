    public void crearLista(ColaFichas cf, ListaFichas aux){
        for (int i = 0; i < 7; i++) {
            nodoFicha ingreso = cf.obtener();
            String letra = ingreso.getLetra();
            cf.popFicha();
            aux.ingresarFicha(letra);
        }
    }

